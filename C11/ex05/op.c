long	ft_multiplication(long a, long b)
{
	return (a * b);
}

long	ft_addition(long a, long b)
{
	return (a + b);
}

long	ft_substraction(long a, long b)
{
	return (a - b);
}

long	ft_division(long a, long b)
{
	return (a / b);
}

long	ft_modulo(long a, long b)
{
	return (a % b);
}
