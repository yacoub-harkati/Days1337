int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (!nb || nb == 1)
		return (nb);
	if (nb <= 2 && nb != 1)
		return (0);
	while (i * i <= nb && i < 46340)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
