int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (i < 0)
	{
		return (-1);
	}
	if (!nb)
	{
		return (0);
	}
	while (i * i <= nb && i != 46340)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (-1);
}
